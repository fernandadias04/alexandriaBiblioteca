
public class Livro 
{
    private String nome;
    private String autor;
    private int nPag;
    private int qtdCopias;
    private int id;

    public Livro(String nome, String autor, int nPag,int qtdCopias, int id)
    {
        this.nome = nome;
        this.autor = autor;
        this.nPag = nPag;
        this.qtdCopias = qtdCopias;
        this.id = id;
    }

}
